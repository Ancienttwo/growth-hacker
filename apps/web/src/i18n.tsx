const dictionaries: Record<I18nLocale, Record<I18nKey, string>> = {
  "zh-Hans": zhHans,
  "zh-Hant": zhHant,
  en
};

const localeOptions: I18nLocale[] = ["zh-Hans", "zh-Hant", "en"];

interface I18nContextValue {
  locale: I18nLocale;
  localeOptions: I18nLocale[];
  setLocale: (locale: I18nLocale) => void;
  t: TFunction;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<I18nLocale>(() => readStoredLocale());

  const setLocale = useCallback((nextLocale: I18nLocale) => {
    setLocaleState(nextLocale);
    localStorage.setItem(i18nStorageKey, nextLocale);
  }, []);

  const t = useCallback<TFunction>((key, params) => translate(key, locale, params), [locale]);

  useEffect(() => {
    document.documentElement.lang = htmlLang(locale);
  }, [locale]);

  const value = useMemo(
    () => ({
      locale,
      localeOptions,
      setLocale,
      t
    }),
    [locale, setLocale, t]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used inside I18nProvider");
  return value;
}

export function translate(key: I18nKey, locale: I18nLocale = defaultI18nLocale, params?: I18nParams): string {
  const template = dictionaries[locale]?.[key] ?? zhHans[key] ?? key;
  if (!params) return template;
  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (match, name: string) => String(params[name] ?? match));
}

export function normalizeI18nLocale(value: string | null | undefined): I18nLocale {
  if (!value) return defaultI18nLocale;
  const normalized = value.toLowerCase().replace("_", "-");
  if (normalized === "zh-hans" || normalized === "zh-cn" || normalized === "zh-sg" || normalized === "zh-my") return "zh-Hans";
  if (normalized === "zh-hant" || normalized === "zh-hk" || normalized === "zh-tw" || normalized === "zh-mo") return "zh-Hant";
  if (normalized === "en" || normalized.startsWith("en-")) return "en";
  return defaultI18nLocale;
}

export function intlLocale(locale: I18nLocale): string {
  if (locale === "zh-Hant") return "zh-Hant";
  if (locale === "en") return "en";
  return "zh-Hans";
}

export function speechLocale(locale: I18nLocale): string {
  if (locale === "zh-Hant") return "zh-HK";
  if (locale === "en") return "en-US";
  return "zh-CN";
}

export function languageLabel(locale: I18nLocale, t: TFunction): string {
  if (locale === "zh-Hant") return t("language.zhHant");
  if (locale === "en") return t("language.en");
  return t("language.zhHans");
}

function readStoredLocale(): I18nLocale {
  return normalizeI18nLocale(localStorage.getItem(i18nStorageKey));
}

function htmlLang(locale: I18nLocale): string {
  if (locale === "zh-Hant") return "zh-Hant";
  if (locale === "en") return "en";
  return "zh-Hans";
}
